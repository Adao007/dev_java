package lab12;

public class Dog {
		
		private String name;
		private int age;
		private String breed;
		
		public Dog(String n, int a, String b)
		{
			name = n;
			age = a;
			breed = b;
		}
		
		public String getName()
		{
			return name;
		}
		public int getAge()
		{
			return age;
		}
		public String getBreed()
		{
			return breed;
		}
		
		
}

